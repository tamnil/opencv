-D CMAKE_INSTALL_PREFIX=/usr \
-D INSTALL_PYTHON3_EXAMPLES=ON \
-D BUILD_opencv_python2=OFF \
-D PYTHON3_PACKAGES_PATH=/usr/lib/python3/dist-packages \
-D PYTHON3_EXECUTABLE=/usr/bin/python3 \
-D PYTHON3_INCLUDE_DIR=/usr/include/python3.8 \
-D PYTHON3_INCLUDE_DIR2=/usr/include/x86_64-linux-gnu/python3.8 \
-D PYTHON3_LIBRARIES=/usr/lib/python3/dist-packages \
-D PYTHON3_NUMPY_INCLUDE_DIRS=/usr/lib/python3.8/dist-packages/numpy/core/include \
